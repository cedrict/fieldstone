subroutine setup_K

use module_parameters, only: K_storage,iproc
use module_timing

implicit none

!==================================================================================================!
!==================================================================================================!
!@@ \subsection{setup\_K}
!@@
!==================================================================================================!

if (iproc==0) then

!==============================================================================!

write(*,'(a)') shift//K_storage

select case(K_storage)

!------------------
case('matrix_FULL')

   call setup_K_matrix_FULL

!------------------
case('matrix_MUMPS')

   call setup_K_matrix_MUMPS

!------------------
case('matrix_CSR')

   call setup_K_matrix_CSR

!------------------
case('blocks_MUMPS')

!------------------
case('blocks_CSR')

!------------------
case default

   stop 'setup_K: unknown K_storage value'

end select

!==============================================================================!

end if ! iproc

end subroutine

